def is_eligible_for_strategy(city: str, strategy_id: str) -> bool:
    """Check if a city is eligible for a given strategy."""
    city_norm = city.strip()

    if city_norm in US_CITIES:
        return False

    if strategy_id == "strategy1":
        return city_norm not in STRATEGY1_EXCLUDED
    elif strategy_id == "strategy2":
        return city_norm not in STRATEGY2_EXCLUDED
    return True


def get_city_coords(city: str):
    """Get coordinates for a city. Returns dict with lat/lon or None."""
    if city in CITY_COORDS:
        return CITY_COORDS[city]
    city_lower = city.lower()
    for name, coords in CITY_COORDS.items():
        if name.lower() == city_lower:
            return coords
    for name, coords in CITY_COORDS.items():
        if city_lower in name.lower() or name.lower() in city_lower:
            return coords
    return None
